/*****************************************************************************
**一个整数，它加上100后是完全平方数，再加上168又是完全平方数请问该数是多少？**
*****************************************************************************/
#include<stdio.h>
#include<math.h>
void main()
{
 int x,y,i;
 for(i=1;i<100000;i++)
 {
 x=sqrt(i+100);
 y=sqrt(i+268);
 if(x*x==(i+100)&&y*y==(i+268))
 {
 printf("%d\n",i);
 }

 }
}